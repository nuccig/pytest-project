class Calculadora:
    """Classe para realizar operações matemáticas básicas."""
    
    @staticmethod
    def soma(a,b):
        return a+b

    @staticmethod
    def subtracao(a,b):
        return a-b
    
    @staticmethod
    def multiplicacao(a,b):
        return a*b
    
    @staticmethod
    def divisao(a,b):
        return a/b